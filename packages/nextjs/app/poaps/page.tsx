const PoapPage: React.FC = () => {
  const [userAddress, setUserAddress] = useState<string>("");
  const [poaps, setPoaps] = useState<any[]>([]);
  const [loading, setLoading] = useState<boolean>(false);

  const { resolvedTheme } = useTheme();
  const [mounted, setMounted] = useState(false);

  const connectWallet = async () => {
    if (typeof window.ethereum !== "undefined") {
      try {
        const provider = new ethers.BrowserProvider(window.ethereum);
        await provider.send("eth_requestAccounts", []);
        const signer = await provider.getSigner();
        setUserAddress(signer.address);
      } catch (error) {
        console.error("Wallet connection error:", error);
      }
    } else {
      alert("MetaMask is not installed.");
    }
  };

  const fetchPOAPs = useCallback(async () => {
    if (!userAddress) return;
    setLoading(true);
    try {
      const provider = new ethers.BrowserProvider(window.ethereum);
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, provider);
      const tokenCounter = await contract.tokenCounter();
      const totalTokens = Number(tokenCounter);
      const userPoaps: any[] = [];
      // Iterate over token IDs; for MVP, this is acceptable.
      for (let tokenId = 1; tokenId < totalTokens; tokenId++) {
        try {
          const owner = await contract.ownerOf(tokenId);
          if (owner.toLowerCase() === userAddress.toLowerCase()) {
            // eslint-disable-next-line @typescript-eslint/no-unused-vars
            const [courseId, courseName, courseDescription, timestamp] = await contract.certificateData(tokenId);
            userPoaps.push({
              tokenId,
              courseId: Number(courseId),
              courseName,
              courseDescription,
              timestamp: new Date(Number(timestamp) * 1000).toLocaleString(),
            });
          }
        } catch (error) {
          console.error(`Error fetching token ${tokenId}:`, error);
        }
      }
      setPoaps(userPoaps);
    } catch (error) {
      console.error("Error fetching POAPs:", error);
    } finally {
      setLoading(false);
    }
  }, [userAddress]);

  useEffect(() => {
    if (userAddress) {
      fetchPOAPs();
    }
  }, [fetchPOAPs, userAddress]);

  useEffect(() => {
    setMounted(true);
  }, []);

  if (!mounted) return null;

  return (
    <div className="container mx-auto p-4">
      <h1 className="text-2xl font-bold mb-4">Your POAP Certificates</h1>
      {!userAddress ? (
        <button
          onClick={connectWallet}
          className={
            resolvedTheme === "dark" ? "bg-[#1F7D53] text-white px-4 py-2" : "bg-[#C5BAFF] text-white px-4 py-2"
          }
        >
          Connect MetaMask
        </button>
      ) : (
        <div>
          <p>Your wallet: {userAddress}</p>
          <button
            onClick={fetchPOAPs}
            className={
              resolvedTheme === "dark"
                ? "bg-[#1F7D53] text-white px-4 py-2 mb-5"
                : "bg-[#C5BAFF] text-white px-4 py-2 mb-5"
            }
          >
            Refresh POAPs
          </button>
          {loading ? (
            <p>Loading your POAPs...</p>
          ) : (
            <div>
              {poaps.length === 0 ? (
                <p>You have no POAP certificates.</p>
              ) : (
                <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
                  {poaps.map(poap => (
                    <div
                      key={poap.tokenId}
                      className={`border p-4 rounded ${resolvedTheme === "dark" ? "" : "border-gray-500"}`}
                    >
                      <h2 className="text-xl font-semibold">Token ID: {poap.tokenId}</h2>
                      <p>
                        <strong>Course ID:</strong> {poap.courseId}
                      </p>
                      <p>
                        <strong>Course Name:</strong> {poap.courseName}
                      </p>
                      <p>
                        <strong>Description:</strong> {poap.courseDescription}
                      </p>
                      <p>
                        <strong>Issued On:</strong> {poap.timestamp}
                      </p>
                    </div>
                  ))}
                </div>
              )}
            </div>
          )}
        </div>
      )}
    </div>
  );
};

export default PoapPage;
